package org.anon.smart.d2cache.store.repository.datasource;

import java.lang.reflect.Field;

public class SQLDescriptor
{
    private String _sql;
    private Field[] _flds;

    public SQLDescriptor()
    {
    }

    public void setSQL(String sql)
    {
        _sql = sql;
    }

    public void setFields(Field[] flds)
    {
        _flds = flds;
    }

    public String sql() { return _sql; }
    public Field[] fields() { return _flds; }
}

